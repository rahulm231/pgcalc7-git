<p><strong>“Estate and Legacy Planning” – Does it Really Matter? </strong></p>
<p>Think about this: early in the life of Israel we see the Lord repeatedly reminding them about the importance of passing on to “their children and their children’s children” God’s testimony of faithfulness (Deuteronomy 4:9 and following).  God went on to tell the children of Israel that He wasn’t talking directly with their children, but rather with them – because “their eyes had seen” all that the Lord had done (Deuteronomy 11:2-7). </p>
<p>Estate planning, when done with intentionality and by including a narrative of one’s life, can be an incredible means of motivating future generations.  Further empowering such planning with tax wise strategies that endow heirs as well as those ministries that have had an impact upon you and your family assures there is a means by which that consistent behavior is facilitated!</p>
<p>Watch for our next installment where we will purpose to unpack …</p>
<ul>
<li>what estate and legacy planning is</li>
<li>why it’s important</li>
<li>what it looks like</li>
</ul>
<p>and most importantly,</p>
<ul>
<li>how it can be a compelling motivation for succeeding generations, when it is empowered with an integrated level of charitable gift planning</li>
</ul>
<p>As always, if we can be of service to you in this area, or for any questions you may have, please do not hesitate to email us at <a href="mailto:legacyplan@letu.edu">legacyplan@letu.edu</a> or call us toll free at 903-500-2972 and ask for Tom Bevan.</p>
<p><em>And always a proper disclaimer: When considering what we might point to by way of a benefit to you in any given situation, always assure to consult your legal and tax advisor for what qualifies as the actual tax treatment of that scenario in your unique situation.</em></p>
