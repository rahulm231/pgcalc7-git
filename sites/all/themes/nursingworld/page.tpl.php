<h2>
 Nurse of the Year: Laurene Elauria Johnson MSN, RN, CDP, of the Department of Veterans Affairs (VA)
 </h2>
 <div class="e-richtext" original-class="e-richtext">
 <p>Laurene Elauria Johnson was nominated for her loyalty and unending compassion for patients, employers and co-workers. A truly remarkable nurse; her story is equally inspirational. Growing up in the Philippines she dreamed of becoming a nurse, and since moving to the United States in 1981, she has dedicated herself to the profession.</p>
<p>Johnson has worked at the Department of Veterans Affairs since 1993, and considers it her responsibility to give back to the people who have sacrificed everything for our country&rsquo;s freedom. Yet Johnson&rsquo;s commitment does not end with her patients - she is a passionate advocate for the future of nursing. As a believer in the power of education, Johnson is serving as a benefactor for her niece&rsquo;s nursing BSN in the Philippines, so that she too can fulfil her dream.&nbsp;</p>
